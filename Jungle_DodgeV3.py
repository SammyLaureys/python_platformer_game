class Button():
    def __init__(self, x, y, image):
        self.image = image
        self.rect = self.image.get_rect()
        self.rect.x = x
        self.rect.y = y
        self.clicked = False

    def draw(self):
        action = False
        #get mouse position
        pos = pygame.mouse.get_pos()

        #check mouse over and click
        if self.rect.collidepoint(pos):
            if pygame.mouse.get_pressed()[0] == 1 and self.clicked == False:
                action = True
                self.clicked = True
        if pygame.mouse.get_pressed()[0] == 0:
            self.clicked = False
        #draw button
        screen.blit(self.image, self.rect)

        return action

class Player():
    def __init__(self, x, y):
        self.reset(x, y)

    def update(self, game_over):
        dx = 0
        dy = 0
        walk_cd = 5
        col_thresh = 20

        if game_over == 0:
            #get keypresses
            key = pygame.key.get_pressed()
            if key[pygame.K_SPACE] and self.jumped == False:
                jump_fx.play()
                self.vel_y = -15
                self.jumped = True
            if key[pygame.K_SPACE] == False and self.in_air == False:
                self.jumped = False
            if key[pygame.K_LEFT] and self.rect.left > 1:
                dx -= self.player_speed
                self.counter += 1
                self.direction = -1
            if key[pygame.K_RIGHT] and self.rect.right < screen_width:
                dx += self.player_speed
                self.counter += 1
                self.direction = 1
            if key[pygame.K_LEFT] == False and key[pygame.K_RIGHT] == False:
                self.counter = 0
                self.index = 0
                if self.direction == 1:
                    self.image = self.images_right[self.index]
                if self.direction == -1:
                    self.image = self.images_left[self.index]

            #animation
            if self.counter > walk_cd:
                self.counter = 0
                self.index += 1
                if self.index >= len(self.images_right):
                    self.index = 0
                if self.direction == 1:
                    self.image = self.images_right[self.index]    
                if self.direction == -1:
                    self.image = self.images_left[self.index]

            #add gravity
            self.vel_y += 1
            if self.vel_y > 10:
                self.vel_y = 10
            dy += self.vel_y

            #check for collision
            self.in_air = True
            for tile in world.tile_list:
                #collision in x direction
                if tile[1].colliderect(self.rect.x + dx, self.rect.y, self.width, self.height):
                    dx = 0
                #collision in y direction
                if tile[1].colliderect(self.rect.x, self.rect.y + dy, self.width, self.height):
                    #hitting your head
                    if self.vel_y < 0:
                        dy = tile[1].bottom - self.rect.top
                        self.vel_y = 0
                    #landing on a tile
                    elif self.vel_y >= 0:
                        dy = tile[1].top - self.rect.bottom
                        self.vel_y = 0
                        self.in_air = False


            #collision with enemy
            if pygame.sprite.spritecollide(self, blob_group, False):
                game_over_fx.play()
                game_over = -1
            
            #collision with lava
            if pygame.sprite.spritecollide(self, lava_group, False):
                game_over_fx.play()
                game_over = -1

            #collision with spike
            if pygame.sprite.spritecollide(self, spike_group, False):
                game_over_fx.play()
                game_over = -1

            #collision with bouncer
            if pygame.sprite.spritecollide(self, bounce_group, False, pygame.sprite.collide_mask):
                self.vel_y = -25
                bouncer_fx.play()
                self.jumped = True

            #check for collision with bullet
            if pygame.sprite.spritecollide(self, bullet_group, False):
                game_over_fx.play()
                game_over = -1

            #collision with portal
            if pygame.sprite.spritecollide(self, portal_group, False):
                game_over = 1

            #check for shrink collection
            if pygame.sprite.spritecollide(self, shrink_group, True):
                self.shrinkCollected = True
                coin_fx.play()

            #collision with platform
            for platform in platform_group:
                #collision in x direction
                if platform.rect.colliderect(self.rect.x + dx, self.rect.y, self.width, self.height):
                    dx = 0
                #collision in y direction
                if platform.rect.colliderect(self.rect.x, self.rect.y + dy, self.width, self.height):
                    #check if below platform
                    if abs((self.rect.top + dy) - platform.rect.bottom) < col_thresh:
                        self.vel_y = 0
                        dy = platform.rect.bottom - self.rect.top
                    #check if above platform
                    elif abs((self.rect.bottom + dy) - platform.rect.top) < col_thresh:
                        self.rect.bottom = platform.rect.top - 1
                        self.in_air = False
                        dy = 0
                    #move sideways with platform
                    if platform.move_x != 0:
                        self.rect.x += platform.direction

            #update player size
            if self.run_once == 0:
                if self.shrinkCollected:
                    self.images_right.clear()
                    self.images_left.clear()
                    for num in range(1, 12):
                        img_right = pygame.image.load(f"images/player_{num}.png")  
                        img_right = pygame.transform.scale(img_right, (20, 40))
                        img_left = pygame.transform.flip(img_right, True, False)
                        self.images_right.append(img_right)
                        self.images_left.append(img_left)
                    self.image = self.images_right[self.index]
                    self.rect = self.image.get_rect()
                    self.width = self.image.get_width()
                    self.height = self.image.get_height()
                    self.rect.x = world.shrink.rect.x
                    self.rect.y = world.shrink.rect.y
                    self.run_once = 1

            #update player position
            self.rect.x += dx
            self.rect.y += dy

        elif game_over == -1:
            self.image = self.dead_image
            draw_text("GAME OVER!", font, red, (screen_width//2) - 167, screen_height//2)
            if self.rect.y > 200:
                self.rect.y -= 5

        screen.blit(self.image, self.rect)
        return game_over

    def reset(self, x, y):
        self.images_right = []
        self.images_left = []
        self.index = 0
        self.counter = 0
        self.shrinkCollected = False

        for num in range(1, 12):
            img_right = pygame.image.load(f"images/player_{num}.png")  
            img_right = pygame.transform.scale(img_right, (40, 80))
            img_left = pygame.transform.flip(img_right, True, False)
            self.images_right.append(img_right)
            self.images_left.append(img_left)

        self.dead_image = pygame.image.load("images/ghost.png")
        self.dead_image = pygame.transform.scale(self.dead_image, (40, 80))
        self.image = self.images_right[self.index]
        self.rect = self.image.get_rect()
        self.rect.x = x
        self.rect.y = y
        self.width = self.image.get_width()
        self.height = self.image.get_height()
        self.vel_y = 0
        self.jumped = False
        self.direction = 0
        self.in_air = True
        self.player_speed = 5
        self.run_once = 0

class World():
    def __init__(self, data):
        self.tile_list = []
        #load images
        dirt_img = pygame.image.load("images/dirt.png")
        grass_img = pygame.image.load("images/grass.png")

        row_count = 0
        for row in data:
            col_count = 0
            for tile in row:
                if tile == 1:
                    img = pygame.transform.scale(dirt_img, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size

#Main game loop
run = True
bananaCollected = False
while run:
    clock.tick(fps)

    screen.blit(bg_img, (0, 0))

    if main_menu == True:
        draw_text("Reach the portals to continue to the next level!", font_score, white, screen_width//2 - 170, 260)
        draw_text("Use arrowkeys and space to move!", font_score, white, screen_width//2 - 100, 300)
        if exit_button.draw():
            run = False
        if start_button.draw():
            main_menu = False
    else:
        world.draw()

        if game_over == 0:
            blob_group.update()
            platform_group.update()

            if bananaCollected:
                player.player_speed = 10
                screen.blit(move_image, (player.rect.x + 12, player.rect.y - 35))
                draw_text("+", font_score, green, player.rect.x, player.rect.y - 28)
            else:
                player.player_speed = 5

            #check if swimming
            if pygame.sprite.spritecollide(player, water_group, False):
                player.player_speed = 2
            #update level text
            draw_text("Level: " + str(level) + "/5", font_score, white, tile_size -10, 10)
            #check for banana collection
            if pygame.sprite.spritecollide(player, banana_group, True):
                bananaCollected = True
                coin_fx.play()

        blob_group.draw(screen)
        bullet_platform_group.draw(screen)
        platform_group.draw(screen)
        lava_group.update()
        lava_group.draw(screen)
        banana_group.draw(screen)
        shrink_group.draw(screen)
        spike_group.update()
        spike_group.draw(screen)
        water_group.update()
        water_group.draw(screen)
        portal_group.draw(screen)
        portal_group.update()
        canon_group.update()
        canon_group.draw(screen)
        bullet_group.update()
        bullet_group.draw(screen)
        bow_group.update()
        bow_group.draw(screen)
        arrow_group.update()
        arrow_group.draw(screen)
        bounce_group.update()
        bounce_group.draw(screen)

        game_over = player.update(game_over)

        #if player died
        if game_over == -1:
            if restart_button.draw():
                world_data = []
                world = reset_level(level)
                game_over = 0
                bananaCollected = False
                player.shrinkCollected = False

        #if player completed the level
        if game_over == 1:
            level += 1
            bananaCollected = False
            player.shrinkCollected = False
            if level <= max_levels:
                world_data = []
                world = reset_level(level)
                game_over = 0
            else:
                draw_text("YOU WIN!", font, green, (screen_width//2) - 130, screen_height//2)
                if restart_button.draw():
                    level = 1
                    world_data = []
                    world = reset_level(level)
                    game_over = 0
                    score = 0

    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            run = False
        if event.type == fire_bullet:
            if level == 2 or level == 5:
                world.canon.fire()
        if event.type == fire_arrow:
            if level == 3 or level == 5:
                world.bow.fire()

    #update display
    pygame.display.update()

#end game
pygame.quit()